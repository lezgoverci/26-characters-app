import { useRouter } from "next/navigation"
import axios from "axios"
import { useEffect, useState } from "react"
import { SkeletonList } from "@/components/skeleton-list"

import { Template } from "@/types"

import { Badge } from "@/components/ui/badge"



export default function Templates() {
  const router = useRouter()

  const [templates, setTemplates] = useState<Template[]>([])
  const [loading, setLoading] = useState(true)

  const fetchTemplates = async () => {
    try {
      const res = await axios.get("https://n8n.xponent.ph/webhook/api/templates/")
      console.log(res)
      if (res.status !== 200) {
        console.error(res.data.error)
        return
      }
      setTemplates(res.data.data)
      setLoading(false)
    } catch (error) {
      console.error(error)
      setLoading(false)
    }
  }

  useEffect(
    () => {
      fetchTemplates()
    },
    []
  )
  return (
    <>
      <header className="flex h-14 items-center justify-between border-b bg-muted/40 px-4 md:px-6">
        <h1 className="text-lg font-semibold">Templates</h1>
        <Button size="sm" onClick={() => router.push("/dashboard/templates/create")}>Create New</Button>
      </header>

      <main className="flex-1 overflow-auto p-4 md:p-6">
        {loading ? <SkeletonList /> : <>
          <Table>
            <TableHeader>
              <TableRow>
                <TableHead>Name</TableHead>
                <TableHead>Link</TableHead>
                <TableHead>Month</TableHead>
                <TableHead>Year</TableHead>
                <TableHead>Premium Template</TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>

              {
                templates.length === 0 &&
                <TableRow>
                  <TableCell colSpan={3} className="text-start">No templates found</TableCell>
                </TableRow>
              }
              {templates.map((template) => (
                <TableRow key={template.id}>
                  <TableCell><Link href={`/dashboard/templates/${template.id}`} className="font-medium underline" prefetch={false}>
                    {template.name}
                  </Link></TableCell>
                  <TableCell>
                    <Button variant="outline" size="sm" onClick={() => window.open(template.link, "_blank")}>Open with Google Slides</Button>
                  </TableCell>
                  <TableCell>{
                    ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"][template.month - 1]
                  }</TableCell>
                  <TableCell>{template.year}</TableCell>
                  <TableCell>{template.status == "completed" ? <Badge className="bg-green-200" variant="outline">Completed</Badge> : <Badge variant="outline">Processing</Badge>}</TableCell>
                </TableRow>
              ))}


            </TableBody>
          </Table>
          {
            templates.length > 0 &&
            <div className="mt-6 flex justify-between">
              <Button variant="outline" size="sm">
                Previous
              </Button>
              <Button variant="outline" size="sm">
                Next
              </Button>
            </div>
          }

        </>
        }
      </main>


    </>
  )
}

